if helyezet == 1:
    print('Gratulálok, arany érmes vagy.')
elif helyezet == 2:
    print('Gratulálok, ezüst érmes lettél.')
elif helyezet == 3:
    print('Gratulálok, bronz érmes lettél.')
elif helyezet<1:
    print('Ne szórakozz velem!')
else:
    print('Gyakorolj mert nem lettél dobogós.')